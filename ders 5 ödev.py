@bot.command()
async def rastgele(ctx):
    img_name = random.choice(os.listdir('images'))
    with open(f'M2L1\images/{img_name}', 'rb') as f:
        picture = discord.File(f)
 
    await ctx.send(file=picture)

bot.run("token")
